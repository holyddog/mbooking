package com.mbooking.common;

public class LogStamp {
	Long cby;
	Long cdate;
	Long eby;
	Long edate;

	public Long getCby() {
		return cby;
	}

	public void setCby(Long cby) {
		this.cby = cby;
	}

	public Long getCdate() {
		return cdate;
	}

	public void setCdate(Long cdate) {
		this.cdate = cdate;
	}

	public Long getEby() {
		return eby;
	}

	public void setEby(Long eby) {
		this.eby = eby;
	}

	public Long getEdate() {
		return edate;
	}

	public void setEdate(Long edate) {
		this.edate = edate;
	}
}